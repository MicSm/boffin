async function assertDirectoryOrMissing(directory, label) {
  const stat = await lstatIfExists(directory);
  if (stat && !stat.isDirectory()) {
    throw new Error(`${label} must be a directory: ${directory}`);
  }
  return stat;
}

async function removeDirectoryIfEmpty(directory) {
  try {
    await fs.rmdir(directory);
  } catch (error) {
    if (!isMissing(error) && error.code !== 'ENOTEMPTY' && error.code !== 'EEXIST') {
      throw error;
    }
  }
}

async function captureFile(filePath, label) {
  const stat = await lstatIfExists(filePath);
  if (!stat) {
    return { exists: false, content: null };
  }
  if (!stat.isFile()) {
    throw new Error(`${label} must be a regular file: ${filePath}`);
  }
  return { exists: true, content: await fs.readFile(filePath) };
}

async function writeFileAtomic(filePath, content) {
  await fs.mkdir(path.dirname(filePath), { recursive: true });
  const token = crypto.randomBytes(6).toString('hex');
  const temporary = path.join(
    path.dirname(filePath),
    `.${path.basename(filePath)}.boffinit-${process.pid}-${token}.tmp`,
  );

  try {
    await fs.writeFile(temporary, content, { flag: 'wx' });
    await fs.rename(temporary, filePath);
  } finally {
    await fs.rm(temporary, { force: true }).catch(() => {});
  }
}

async function restoreFile(filePath, snapshot) {
  if (snapshot.exists) {
    await writeFileAtomic(filePath, snapshot.content);
  } else {
    await fs.rm(filePath, { force: true });
  }
}

async function resolvePreviousPackManagedFiles(resolvedTarget, packFiles, knownPackManifest) {
  const previousManagedFiles = new Set(
    (knownPackManifest?.managedFiles ?? []).filter((file) => file.startsWith('.boffin/packs/')),
  );

  // With a manifest that lists packs (Cursor), trust its list so an incomplete
  // manifest still trips the unknown-file conflict guard. Without one, treat
  // on-disk packs that overlap the source tree as replaceable, since both hosts
  // vendor byte-identical packs for the same boffinit version.
  if (knownPackManifest) {
    return previousManagedFiles;
  }

  const livePacks = path.join(resolvedTarget, '.boffin', 'packs');
  for (const file of packFiles) {
    const relative = toPosixPath(file);
    const absolute = path.join(livePacks, ...relative.split('/'));
    if (await lstatIfExists(absolute)) {
      previousManagedFiles.add(`.boffin/packs/${relative}`);
    }
  }
  return previousManagedFiles;
}

function isSharedManagedPath(managedFile) {
  return managedFile === VERSION_PATH || managedFile.startsWith('.boffin/packs/');
}

function normalizeInstructionEntry(value) {
  if (typeof value !== 'string') {
    return null;
  }
  let normalized = toPosixPath(value).trim();
  while (normalized.startsWith('./')) {
    normalized = normalized.slice(2);
  }
  return path.posix.normalize(normalized);
}

async function mergeOpenCodeConfig(resolvedTarget, previousConfig = null) {
  const configPath = path.join(resolvedTarget, 'opencode.json');
  const jsoncPath = path.join(resolvedTarget, 'opencode.jsonc');
  const configStat = await lstatIfExists(configPath);
  const jsoncStat = await lstatIfExists(jsoncPath);

  if (!configStat && jsoncStat) {
    return {
      ok: false,
      note: JSONC_SKIP_NOTE,
      opencodeConfig: null,
    };
  }

  let created = false;
  let parsed;

  if (!configStat) {
    created = true;
    parsed = {};
  } else {
    // Preserve first-install ownership so uninstall can still delete a file we created.
    created = previousConfig?.created === true;
    if (!configStat.isFile()) {
      return {
        ok: false,
        note: `Skipped opencode.json: ${configPath} is not a regular file`,
        opencodeConfig: null,
      };
    }
    try {
      parsed = JSON.parse(await fs.readFile(configPath, 'utf8'));
    } catch (error) {
      return {
        ok: false,
        note: `Skipped opencode.json: parse failed (${error.message})`,
        opencodeConfig: null,
      };
    }
  }

  if (!parsed || typeof parsed !== 'object' || Array.isArray(parsed)) {
    return {
      ok: false,
      note: 'Skipped opencode.json: root must be a JSON object',
      opencodeConfig: null,
    };
  }

  if (!Object.hasOwn(parsed, '$schema')) {
    parsed.$schema = 'https://opencode.ai/config.json';
  }

  if (!Object.hasOwn(parsed, 'instructions')) {
    parsed.instructions = [];
  } else if (!Array.isArray(parsed.instructions)) {
    return {
      ok: false,
      note: 'Skipped opencode.json: "instructions" must be an array',
      opencodeConfig: null,
    };
  }

  const alreadyPresent = parsed.instructions.some(
    (entry) => normalizeInstructionEntry(entry) === OPENCODE_INSTRUCTION,
  );
  if (!alreadyPresent) {
    parsed.instructions.push(OPENCODE_INSTRUCTION);
  }

  await writeFileAtomic(configPath, `${JSON.stringify(parsed, null, 2)}\n`);
  return {
    ok: true,
    note: null,
    opencodeConfig: {
      path: 'opencode.json',
      created,
      instruction: OPENCODE_INSTRUCTION,
    },
  };
}

async function reverseOpenCodeConfig(resolvedTarget, opencodeConfig) {
  if (!opencodeConfig) {
    return;
  }

  const configPath = path.join(resolvedTarget, opencodeConfig.path);
  const stat = await lstatIfExists(configPath);
  if (!stat || !stat.isFile()) {
    return;
  }

  let parsed;
  try {
    parsed = JSON.parse(await fs.readFile(configPath, 'utf8'));
  } catch {
    return;
  }

  if (!parsed || typeof parsed !== 'object' || Array.isArray(parsed)) {
    return;
  }

  if (Array.isArray(parsed.instructions)) {
    parsed.instructions = parsed.instructions.filter(
      (entry) => normalizeInstructionEntry(entry) !== opencodeConfig.instruction,
    );
  }

  const keys = Object.keys(parsed);
  const onlySchema = keys.length === 1 && keys[0] === '$schema';
  const onlyEmptyInstructions = keys.length === 1
    && keys[0] === 'instructions'
    && Array.isArray(parsed.instructions)
    && parsed.instructions.length === 0;
  const onlySchemaAndEmptyInstructions = keys.length === 2
    && Object.hasOwn(parsed, '$schema')
    && Object.hasOwn(parsed, 'instructions')
    && Array.isArray(parsed.instructions)
    && parsed.instructions.length === 0;

  if (
    opencodeConfig.created
    && (keys.length === 0 || onlySchema || onlyEmptyInstructions || onlySchemaAndEmptyInstructions)
  ) {
    await fs.rm(configPath, { force: true });
    return;
  }

  if (!opencodeConfig.created && keys.length === 0) {
    await writeFileAtomic(configPath, '{}\n');
    return;
  }

  await writeFileAtomic(configPath, `${JSON.stringify(parsed, null, 2)}\n`);
}

// Publishes the shared `.boffin/packs` tree rebuild-then-swap style and returns a
// handle to either commit (drop the temp/backup) or roll back to the prior tree.
// Owns only the packs tree and its temp scratch; the caller owns VERSION, its own
// managed files, and all directory teardown so ownership and ordering stay local.
async function publishSharedPacks(options) {
  const {
    resolvedTarget,
    packsDirectory,
    packFiles,
    knownPackManifest,
    boffinDirectory,
  } = options;

  const livePacks = path.join(boffinDirectory, 'packs');
  await assertDirectoryOrMissing(livePacks, '.boffin/packs');
  const previousManagedFiles = await resolvePreviousPackManagedFiles(
    resolvedTarget,
    packFiles,
    knownPackManifest,
  );

  await fs.mkdir(boffinDirectory, { recursive: true });
  const temporaryRoot = await fs.mkdtemp(path.join(boffinDirectory, '.boffinit-tmp-'));
  const stagedPacks = path.join(temporaryRoot, 'packs');
  let backupPacks = null;
  let packsPublished = false;

  try {
    await copyEntry(packsDirectory, stagedPacks);
    await mergeUnknownPackFiles(livePacks, stagedPacks, previousManagedFiles);
    await removeEmptyDirectories(stagedPacks);

    if (await lstatIfExists(livePacks)) {
      backupPacks = path.join(temporaryRoot, 'previous-packs');
      await fs.rename(livePacks, backupPacks);
    }

    try {
      await fs.rename(stagedPacks, livePacks);
      packsPublished = true;
    } catch (error) {
      if (backupPacks) {
        await fs.rename(backupPacks, livePacks);
        backupPacks = null;
      }
      throw error;
    }
  } catch (error) {
    await fs.rm(temporaryRoot, { recursive: true, force: true }).catch(() => {});
    throw error;
  }

  return {
    async commit() {
      await fs.rm(temporaryRoot, { recursive: true, force: true }).catch(() => {});
    },
    async rollback(rollbackErrors) {
      let canRestoreBackup = !packsPublished;
      if (packsPublished) {
        try {
          await fs.rm(livePacks, { recursive: true, force: true });
          packsPublished = false;
          canRestoreBackup = true;
        } catch (rollbackError) {
          rollbackErrors.push(rollbackError.message);
        }
      }
      let preserved = false;
      if (backupPacks && await lstatIfExists(backupPacks)) {
        if (canRestoreBackup) {
          try {
            await fs.rename(backupPacks, livePacks);
            backupPacks = null;
          } catch (rollbackError) {
            rollbackErrors.push(rollbackError.message);
          }
        }
        if (backupPacks) {
          preserved = true;
          rollbackErrors.push(`previous pack tree preserved at ${backupPacks}`);
        }
      }
      if (!preserved) {
        await fs.rm(temporaryRoot, { recursive: true, force: true }).catch(() => {});
      }
    },
  };
}

async function applyInstall(action, options = {}) {
  const {
    target,
    dryRun = false,
    sourceRoot = DEFAULT_SOURCE_ROOT,
    now = () => new Date(),
  } = options;
  if (!target) {
    throw new Error('A target path is required');
  }

  const resolvedTarget = path.resolve(target);
  const previousManifest = await readManifest(resolvedTarget);
  if (action === 'update' && !previousManifest) {
    throw new Error(
      `No boffinit installation found at ${resolvedTarget}; run "boffinit cursor install --target <path>" first`,
    );
  }

  const source = await loadInstallSource(path.resolve(sourceRoot));
  const managedFiles = buildManagedFiles(source.packFiles);
  const manifest = createManifest(managedFiles, previousManifest, now);

  if (dryRun) {
    return {
      action,
      changed: false,
      dryRun: true,
      managedFiles,
      target: resolvedTarget,
      version: PACKAGE_VERSION,
    };
  }

  const targetExisted = Boolean(
    await assertDirectoryOrMissing(resolvedTarget, 'Target'),
  );
  const boffinDirectory = path.join(resolvedTarget, '.boffin');
  const cursorDirectory = path.join(resolvedTarget, '.cursor');
  const rulesDirectory = path.join(cursorDirectory, 'rules');
  const boffinExisted = Boolean(
    await assertDirectoryOrMissing(boffinDirectory, '.boffin'),
  );
  const cursorExisted = Boolean(
    await assertDirectoryOrMissing(cursorDirectory, '.cursor'),
  );
  const rulesExisted = Boolean(
    await assertDirectoryOrMissing(rulesDirectory, '.cursor/rules'),
  );

  const manifestFile = managedPathToAbsolute(resolvedTarget, MANIFEST_PATH);
  const versionFile = managedPathToAbsolute(resolvedTarget, VERSION_PATH);
  const snapshots = new Map();
  snapshots.set(versionFile, await captureFile(versionFile, 'Installed VERSION'));
  for (const filename of RULE_FILENAMES) {
    const ruleFile = path.join(rulesDirectory, filename);
    snapshots.set(ruleFile, await captureFile(ruleFile, 'Cursor rule'));
  }

  let packPublish;
  const publishedFiles = [];
  let succeeded = false;

  try {
    packPublish = await publishSharedPacks({
      resolvedTarget,
      packsDirectory: source.packsDirectory,
      packFiles: source.packFiles,
      knownPackManifest: previousManifest,
      boffinDirectory,
    });
    await writeFileAtomic(versionFile, `${PACKAGE_VERSION}\n`);
    publishedFiles.push(versionFile);

    for (const [filename, content] of source.rules) {
      const ruleFile = path.join(rulesDirectory, filename);
      await writeFileAtomic(ruleFile, content);
      publishedFiles.push(ruleFile);
    }

    await writeFileAtomic(
      manifestFile,
      `${JSON.stringify(manifest, null, 2)}\n`,
    );
    succeeded = true;
  } catch (error) {
    const rollbackErrors = [];
    for (const filePath of publishedFiles.reverse()) {
      try {
        await restoreFile(filePath, snapshots.get(filePath));
      } catch (rollbackError) {
        rollbackErrors.push(rollbackError.message);
      }
    }
    if (packPublish) {
      await packPublish.rollback(rollbackErrors);
    }
    if (rollbackErrors.length > 0) {
      error.message += `; rollback also failed: ${rollbackErrors.join('; ')}`;
    }
    throw error;
  } finally {
    if (packPublish && succeeded) {
      await packPublish.commit();
    }
    if (!succeeded) {
      if (!rulesExisted) {
        await removeDirectoryIfEmpty(rulesDirectory).catch(() => {});
      }
      if (!cursorExisted) {
        await removeDirectoryIfEmpty(cursorDirectory).catch(() => {});
      }
      if (!boffinExisted) {
        await removeDirectoryIfEmpty(boffinDirectory).catch(() => {});
      }
      if (!targetExisted) {
        await removeDirectoryIfEmpty(resolvedTarget).catch(() => {});
      }
    }
  }

  return {
    action,
    changed: true,
    dryRun: false,
    managedFiles,
    target: resolvedTarget,
    version: PACKAGE_VERSION,
  };
}

async function applyOpenCodeInstall(action, options = {}) {
  const {
    target,
    dryRun = false,
    sourceRoot = DEFAULT_SOURCE_ROOT,
    now = () => new Date(),
  } = options;
  if (!target) {
    throw new Error('A target path is required');
  }

  const resolvedTarget = path.resolve(target);
  const previousManifest = await readOpenCodeManifest(resolvedTarget);
  if (action === 'update' && !previousManifest) {
    throw new Error(
      `No boffinit OpenCode installation found at ${resolvedTarget}; run "boffinit opencode install --target <path>" first`,
    );
  }

  const source = await loadOpenCodeSource(path.resolve(sourceRoot));
  const managedFiles = [...OPENCODE_MANAGED_FILES];

  if (dryRun) {
    return {
      action,
      changed: false,
      dryRun: true,
      host: 'opencode',
      managedFiles,
      notes: [],
      target: resolvedTarget,
      version: PACKAGE_VERSION,
    };
  }

  const targetExisted = Boolean(
    await assertDirectoryOrMissing(resolvedTarget, 'Target'),
  );
  const boffinDirectory = path.join(resolvedTarget, '.boffin');
  const openCodeDirectory = path.join(resolvedTarget, '.opencode');
  const skillsDirectory = path.join(openCodeDirectory, 'skills');
  const commandsDirectory = path.join(openCodeDirectory, 'commands');
  const boffinExisted = Boolean(
    await assertDirectoryOrMissing(boffinDirectory, '.boffin'),
  );
  const openCodeExisted = Boolean(
    await assertDirectoryOrMissing(openCodeDirectory, '.opencode'),
  );
  const skillsExisted = Boolean(
    await assertDirectoryOrMissing(skillsDirectory, '.opencode/skills'),
  );
  const commandsExisted = Boolean(
    await assertDirectoryOrMissing(commandsDirectory, '.opencode/commands'),
  );

  const agentsFile = openCodeManagedPathToAbsolute(resolvedTarget, BOFFIN_AGENTS_PATH);
  const profileFile = openCodeManagedPathToAbsolute(resolvedTarget, PROFILE_PATH);
  const manifestFile = openCodeManagedPathToAbsolute(resolvedTarget, OPENCODE_MANIFEST_PATH);
  const versionFile = openCodeManagedPathToAbsolute(resolvedTarget, VERSION_PATH);
  const configPath = path.join(resolvedTarget, 'opencode.json');
  const snapshots = new Map();
  snapshots.set(versionFile, await captureFile(versionFile, 'Installed VERSION'));
  snapshots.set(agentsFile, await captureFile(agentsFile, 'Boffin AGENTS.md'));
  snapshots.set(profileFile, await captureFile(profileFile, 'Boffin profile'));
  snapshots.set(configPath, await captureFile(configPath, 'opencode.json'));
  for (const managedFile of OPENCODE_MANAGED_FILES) {
    const absolute = openCodeManagedPathToAbsolute(resolvedTarget, managedFile);
    snapshots.set(absolute, await captureFile(absolute, 'OpenCode managed file'));
  }

  const notes = [];
  let packPublish;
  const publishedFiles = [];
  let configResult = { ok: false, note: null, opencodeConfig: null };
  let configWritten = false;
  let succeeded = false;

  try {
    packPublish = await publishSharedPacks({
      resolvedTarget,
      packsDirectory: source.packsDirectory,
      packFiles: source.packFiles,
      knownPackManifest: null,
      boffinDirectory,
    });
    await writeFileAtomic(versionFile, `${PACKAGE_VERSION}\n`);
    publishedFiles.push(versionFile);

    await writeFileAtomic(agentsFile, source.agentsContent);
    publishedFiles.push(agentsFile);

    if (!(await lstatIfExists(profileFile))) {
      await writeFileAtomic(profileFile, 'full\n');
      publishedFiles.push(profileFile);
    }

    for (const [relative, content] of source.skills) {
      const skillFile = path.join(skillsDirectory, ...relative.split('/'));
      await writeFileAtomic(skillFile, content);
      publishedFiles.push(skillFile);
    }
    for (const [filename, content] of source.commands) {
      const commandFile = path.join(commandsDirectory, filename);
      await writeFileAtomic(commandFile, content);
      publishedFiles.push(commandFile);
    }

    configResult = await mergeOpenCodeConfig(
      resolvedTarget,
      previousManifest?.opencodeConfig ?? null,
    );
    if (configResult.note) {
      notes.push(configResult.note);
    }
    if (configResult.ok) {
      configWritten = true;
    }

    const manifest = createOpenCodeManifest(
      previousManifest,
      now,
      configResult.opencodeConfig,
    );
    await writeFileAtomic(
      manifestFile,
      `${JSON.stringify(manifest, null, 2)}\n`,
    );
    succeeded = true;
  } catch (error) {
    const rollbackErrors = [];
    for (const filePath of publishedFiles.reverse()) {
      try {
        await restoreFile(filePath, snapshots.get(filePath) ?? { exists: false, content: null });
      } catch (rollbackError) {
        rollbackErrors.push(rollbackError.message);
      }
    }
    if (configWritten) {
      try {
        await restoreFile(configPath, snapshots.get(configPath));
      } catch (rollbackError) {
        rollbackErrors.push(rollbackError.message);
      }
    }
    if (packPublish) {
      await packPublish.rollback(rollbackErrors);
    }
    if (rollbackErrors.length > 0) {
      error.message += `; rollback also failed: ${rollbackErrors.join('; ')}`;
    }
    throw error;
  } finally {
    if (packPublish && succeeded) {
      await packPublish.commit();
    }
    if (!succeeded) {
      if (!commandsExisted) {
        await removeDirectoryIfEmpty(commandsDirectory).catch(() => {});
      }
      if (!skillsExisted) {
        await removeDirectoryIfEmpty(skillsDirectory).catch(() => {});
      }
      if (!openCodeExisted) {
        await removeDirectoryIfEmpty(openCodeDirectory).catch(() => {});
      }
      if (!boffinExisted) {
        await removeDirectoryIfEmpty(boffinDirectory).catch(() => {});
      }
      if (!targetExisted) {
        await removeDirectoryIfEmpty(resolvedTarget).catch(() => {});
      }
    }
  }

  return {
    action,
    changed: true,
    dryRun: false,
    host: 'opencode',
    managedFiles,
    notes,
    opencodeConfig: configResult.opencodeConfig,
    target: resolvedTarget,
    version: PACKAGE_VERSION,
  };
}

