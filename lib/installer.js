const MANAGED_YAML_COMMENT =
  '# BOFFIN-MANAGED: generated by boffinit; local edits may be replaced.';
const MANAGED_HTML_COMMENT =
  '<!-- BOFFIN-MANAGED: generated by boffinit; local edits may be replaced. -->';
const INSTALL_ROOT_LINE =
  'Boffin install root: `.boffin/` in this project. Resolve contract `packs/...` paths against `.boffin/packs/...`.';

function isMissing(error) {
  return error && error.code === 'ENOENT';
}

async function lstatIfExists(filePath) {
  try {
    return await fs.lstat(filePath);
  } catch (error) {
    if (isMissing(error)) {
      return null;
    }
    throw error;
  }
}

async function pathExists(filePath) {
  return Boolean(await lstatIfExists(filePath));
}

function rewritePackReferences(content) {
  return content.replace(/(^|[^\w./-])packs\//gm, '$1.boffin/packs/');
}

function generateManagedRule(content) {
  let generated = rewritePackReferences(content);
  if (generated.includes('BOFFIN-MANAGED: generated by boffinit')) {
    return generated;
  }

  const eol = generated.includes('\r\n') ? '\r\n' : '\n';
  const frontmatterStart = `---${eol}`;
  if (generated.startsWith(frontmatterStart)) {
    generated = `${frontmatterStart}${MANAGED_YAML_COMMENT}${eol}${generated.slice(frontmatterStart.length)}`;
  } else {
    generated = `${MANAGED_HTML_COMMENT}${eol}${generated}`;
  }
  return generated;
}

function generateBoffinAgents(content) {
  const rewritten = rewritePackReferences(content);
  const eol = rewritten.includes('\r\n') ? '\r\n' : '\n';
  return `${MANAGED_HTML_COMMENT}${eol}${INSTALL_ROOT_LINE}${eol}${eol}${rewritten}`;
}

function validateManifest(manifest) {
  if (!manifest || typeof manifest !== 'object' || Array.isArray(manifest)) {
    throw new Error('manifest root must be an object');
  }
  if (manifest.schemaVersion !== SCHEMA_VERSION) {
    throw new Error(`unsupported schemaVersion ${String(manifest.schemaVersion)}; expected 1`);
  }
  if (typeof manifest.version !== 'string' || manifest.version.length === 0) {
    throw new Error('version must be a non-empty string');
  }
  if (manifest.installMethod !== 'cursor') {
    throw new Error('installMethod must be "cursor"');
  }
  if (
    typeof manifest.installedAt !== 'string'
    || Number.isNaN(Date.parse(manifest.installedAt))
  ) {
    throw new Error('installedAt must be an ISO date string');
  }
  if (!Array.isArray(manifest.managedFiles)) {
    throw new Error('managedFiles must be an array');
  }

  const managedFiles = manifest.managedFiles.map(validateManagedPath);
  if (new Set(managedFiles).size !== managedFiles.length) {
    throw new Error('managedFiles must not contain duplicates');
  }

  return {
    schemaVersion: SCHEMA_VERSION,
    version: manifest.version,
    installMethod: 'cursor',
    installedAt: manifest.installedAt,
    managedFiles,
  };
}

function validateOpenCodeManifest(manifest) {
  if (!manifest || typeof manifest !== 'object' || Array.isArray(manifest)) {
    throw new Error('manifest root must be an object');
  }
  if (manifest.schemaVersion !== SCHEMA_VERSION) {
    throw new Error(`unsupported schemaVersion ${String(manifest.schemaVersion)}; expected 1`);
  }
  if (typeof manifest.version !== 'string' || manifest.version.length === 0) {
    throw new Error('version must be a non-empty string');
  }
  if (manifest.installMethod !== 'opencode') {
    throw new Error('installMethod must be "opencode"');
  }
  if (
    typeof manifest.installedAt !== 'string'
    || Number.isNaN(Date.parse(manifest.installedAt))
  ) {
    throw new Error('installedAt must be an ISO date string');
  }
  if (!Array.isArray(manifest.managedFiles)) {
    throw new Error('managedFiles must be an array');
  }

  const managedFiles = manifest.managedFiles.map(validateOpenCodeManagedPath);
  if (new Set(managedFiles).size !== managedFiles.length) {
    throw new Error('managedFiles must not contain duplicates');
  }

  let opencodeConfig = null;
  if (manifest.opencodeConfig !== undefined && manifest.opencodeConfig !== null) {
    if (
      typeof manifest.opencodeConfig !== 'object'
      || Array.isArray(manifest.opencodeConfig)
    ) {
      throw new Error('opencodeConfig must be an object');
    }
    if (manifest.opencodeConfig.path !== 'opencode.json') {
      throw new Error('opencodeConfig.path must be "opencode.json"');
    }
    if (typeof manifest.opencodeConfig.created !== 'boolean') {
      throw new Error('opencodeConfig.created must be a boolean');
    }
    if (manifest.opencodeConfig.instruction !== OPENCODE_INSTRUCTION) {
      throw new Error(`opencodeConfig.instruction must be "${OPENCODE_INSTRUCTION}"`);
    }
    opencodeConfig = {
      path: 'opencode.json',
      created: manifest.opencodeConfig.created,
      instruction: OPENCODE_INSTRUCTION,
    };
  }

  return {
    schemaVersion: SCHEMA_VERSION,
    version: manifest.version,
    installMethod: 'opencode',
    installedAt: manifest.installedAt,
    managedFiles,
    boffinAgents: BOFFIN_AGENTS_PATH,
    profileFile: PROFILE_PATH,
    opencodeConfig,
  };
}

async function readManifest(target) {
  const manifestFile = managedPathToAbsolute(target, MANIFEST_PATH);
  const stat = await lstatIfExists(manifestFile);
  if (!stat) {
    return null;
  }
  if (!stat.isFile()) {
    throw new Error(`Invalid boffinit manifest at ${manifestFile}: expected a regular file`);
  }

  let parsed;
  try {
    parsed = JSON.parse(await fs.readFile(manifestFile, 'utf8'));
  } catch (error) {
    throw new Error(`Invalid boffinit manifest at ${manifestFile}: ${error.message}`);
  }

  try {
    return validateManifest(parsed);
  } catch (error) {
    throw new Error(`Invalid boffinit manifest at ${manifestFile}: ${error.message}`);
  }
}

