async function removeEmptyManagedDirectories(target, managedFiles) {
  const resolvedTarget = path.resolve(target);
  const directories = new Set();
  for (const managedFile of managedFiles) {
    let current = path.dirname(projectPathToAbsolute(resolvedTarget, managedFile));
    while (current !== resolvedTarget) {
      directories.add(current);
      const parent = path.dirname(current);
      if (parent === current) {
        break;
      }
      current = parent;
    }
  }

  const deepestFirst = [...directories].sort(
    (left, right) => right.split(path.sep).length - left.split(path.sep).length,
  );
  for (const directory of deepestFirst) {
    try {
      await fs.rmdir(directory);
    } catch (error) {
      if (!isMissing(error) && error.code !== 'ENOTEMPTY' && error.code !== 'EEXIST') {
        throw error;
      }
    }
  }
}

async function uninstall(options = {}) {
  const { target, dryRun = false } = options;
  if (!target) {
    throw new Error('A target path is required');
  }

  const resolvedTarget = path.resolve(target);
  const manifest = await readManifest(resolvedTarget);
  if (!manifest) {
    return {
      action: 'uninstall',
      changed: false,
      dryRun,
      managedFiles: [],
      target: resolvedTarget,
      version: PACKAGE_VERSION,
    };
  }

  const keepShared = await pathExists(
    projectPathToAbsolute(resolvedTarget, OPENCODE_MANIFEST_PATH),
  );
  const managedFiles = [...new Set([...manifest.managedFiles, MANIFEST_PATH])];
  const filesToRemove = managedFiles.filter((managedFile) => {
    if (managedFile === MANIFEST_PATH) {
      return true;
    }
    if (keepShared && isSharedManagedPath(managedFile)) {
      return false;
    }
    return true;
  });

  for (const managedFile of filesToRemove) {
    const filePath = managedPathToAbsolute(resolvedTarget, managedFile);
    const stat = await lstatIfExists(filePath);
    if (stat && !stat.isFile() && !stat.isSymbolicLink()) {
      throw new Error(
        `Refusing to uninstall because a managed file became a directory: ${filePath}`,
      );
    }
  }

  if (dryRun) {
    return {
      action: 'uninstall',
      changed: false,
      dryRun: true,
      managedFiles: filesToRemove,
      target: resolvedTarget,
      version: PACKAGE_VERSION,
    };
  }

  for (const managedFile of filesToRemove) {
    if (managedFile === MANIFEST_PATH) {
      continue;
    }
    await fs.rm(managedPathToAbsolute(resolvedTarget, managedFile), { force: true });
  }
  await fs.rm(managedPathToAbsolute(resolvedTarget, MANIFEST_PATH), { force: true });
  await removeEmptyManagedDirectories(resolvedTarget, filesToRemove);

  return {
    action: 'uninstall',
    changed: true,
    dryRun: false,
    managedFiles: filesToRemove,
    target: resolvedTarget,
    version: PACKAGE_VERSION,
  };
}

